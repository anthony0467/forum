<h1>Réglement du forum</h1>

<h2>Généralités</h2>
<p>
Nous rappelons que l'auteur d'un message est responsable des propos qu'il publie. En cas de manquement grave à la charte ou des lois et règlements en vigueur en France, ce dernier s'expose à une suppression de ses messages, voire de son compte, ainsi qu'aux sanctions civiles ou pénales afférentes.

MyForum.fr ne peut être tenu pour responsable quant à l'agissement de certains membres sur le forum. Si vous trouvez des messages hors-charte vous pouvez nous les signaler via le formulaire de contact.

En participant aux forums de Previssima.fr, vous autorisez les administrateurs et les modérateurs du site à supprimer ou verrouiller n'importe quel message pour n'importe quelles raisons notamment exposées dans cette charte, sans autorisation préalable de votre part.
</p>
<h2>Contenus non autorisés</h2>
<p>
- Les messages à caractères pornographique et pédopornographique.

- Les messages racistes, xénophobes, révisionnistes, faisant l'apologie de crime de guerre, discriminant ou incitant à la haine qu’elle soit à l'encontre d'une personne, d'un groupe de personnes en raison de leurs origines, leur ethnie, leurs croyances ou leur mode de vie.

- Les messages à caractère insultants, violents, menaçants, au contenu choquant ou portant atteinte à la dignité humaine.

- Les messages diffamatoires.

- Les messages bafouant le droit d'auteur, le droit à l'image et le respect à la vie privée.

- Les messages dans le but de nuire au forum tel que le spam ou bien ceux générant une mauvaise ambiance ou un mauvais esprit.

- Les liens de parrainages et les publicités, qu'elles soient commerciales ou non. La mention de professionnel (ex : courtier) peut-être indiqué dans les messages, mais aucun lien ou caractère promotionnel ne peut être inséré.

- Les démarchages, de manière générale, sont interdits et peuvent conduire à la suppression du compte.

- Et de manière plus générale, tous les messages contraires aux lois en vigueur en France.
</p>

<h2>Bonnes pratiques</h2>
<p>
Afin que les forums restent un endroit agréable à consulter, merci de suivre les bonnes pratiques ci-dessous.
</p>
<h3>Utilisez la fonction "Rechercher" du Forum</h3>
<p>
Avant de poster un message, vérifiez d'abord si l'on n'a pas déjà répondu à votre question sur un autre sujet, ou si le sujet n'a pas déjà été abordé. Vous trouverez sur le forum un moteur de recherche qui facilitera vos investigations.
</P>

<h3>Evitez les majuscules</h3>
<p>
Les majuscules sur les forums sont considérées comme un cri, ou un signe d'énervement. Donc même si vous pensez que ça rendra votre sujet plus visible, évitez les majuscules. Vous allez paraître énervé et vous n'allez pas encourager les autres à participer à votre discussion.</p>

<h3>Evitez le langage SMS</h3>
<p>
Vous êtes sur un forum, vous n'êtes pas limité en nombre de caractères. Essayez de formuler vos posts en français correct de manière à vous faire comprendre du plus grand nombre.</p>

<h3>Bien choisir le titre du sujet</h3>
<p>
Pour obtenir une aide efficace et rapide facilitez la démarche aux éventuelles réponses. D'abord pensez à rédiger un sujet explicite, de préférence sous forme de question : dans le titre, mettez tous les détails utiles pour situer le problème. Evitez les messages du type : "aide", "besoin d'aide"... Précisez ce que vous demandez , par exemple : "Comment contester le montant de mes cotisations URSSAF artisan ?" ou encore "Problème de réception de ma carte vitale"... Un sujet explicite permettra aux personnes qui peuvent vous aider de ne pas passer à côté de votre sujet, et aussi de servir aux prochains subissant le même problème, grâce au moteur de recherche du site.</p>

<h3>Bien décrire le problème</h3>
<p>
Lors de la rédaction du sujet, pensez à donner tous les détails pour que l'on puisse vous aider. Une description détaillé et mise en forme facilitera la lecture par d’autres membres et donc les réponses. Pensez à décrire votre statut (salarié, commerçant…).</p>

<h3>Un peu de politesse</h3>
<p>
Si vous voulez encourager les autres à vous répondre, ne négligez pas les formules de politesse. Ce n'est pas parce que vous êtes sur un forum qu'il faut oublier que vous parlez à des gens que vous ne connaissez pas. Un simple "bonjour", "s'il vous plaît et "Merci" ne coûte rien et bien au contraire encouragera les autres à répondre.</p>

<h3>Gardez votre calme</h3>
<p>
Si par exemple on ne répond pas à votre sujet, ou que la réponse est erronée vérifiez d'abord que vous avez bien décrit le problème. Lors d'une discussion, essayez de ne pas trop vous emporter, critiquez les arguments, et n'attaquez pas la personne. Soyez constructifs. En cas de provocation, le compte de l’internaute pourra être supprimé. Respectez toujours les autres membres si vous voulez être respecté en retour.</p>

<h3>Informations personnelles</h3>
<p>
Hormis les informations de votre profil, ne donnez jamais d'informations pouvant vous causer préjudice (numéros de CB, numéro de carte d’identité…). Si quelqu'un divulguait ces informations sans votre accord, vous êtes en droit de demander la suppression immédiate du message.</p>